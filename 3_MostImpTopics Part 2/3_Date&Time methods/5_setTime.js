//! setTime: using for setting the Time inplace of current live time

    const curTime = new Date();

//! 1. setHours: seting hour inplace of current hour
console.log(curTime);
curTime.setHours(23)
console.log(curTime);

//! 2. setMinutes: setting minutes inplace of current minutes.
console.log(curTime);
curTime.setMinutes(50)
console.log(curTime);

